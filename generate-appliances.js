// Generate appliances
let generatedCount = 0;
let attemptCount = 0;
const maxAttempts = 10000; // Prevent infinite loop

while (generatedCount < 500 && attemptCount < maxAttempts) {
  attemptCount++;
  
  // Select random category
  const category = categories[Math.floor(Math.random() * categories.length)];
  const templates = baseTemplates[category];
  
  if (!templates || templates.length === 0) continue;
  
  // Select random template
  const template = templates[Math.floor(Math.random() * templates.length)];
  
  // Add variation to make unique
  const variations = ['Pro', 'Plus', 'Elite', 'Premium', 'Advanced', 'Basic', 'Standard', 'Compact', 'Portable', 'Mini', 'Max', 'Ultra', 'Smart', 'Digital', 'Automatic', 'Manual'];
  const variation = variations[Math.floor(Math.random() * variations.length)];
  
  let newName;
  if (Math.random() > 0.5 && !template.name.includes(variation)) {
    newName = `${template.name} ${variation}`;
  } else {
    newName = template.name;
  }
  
  const newSlug = generateSlug(newName);
  
  // Check if duplicate
  if (!existingSlugs.has(newSlug) && !existingNames.has(newName.toLowerCase())) {
    const appliance = {
      name: newName,
      slug: newSlug,
      wattage: template.wattage,
      category: category,
      description: `${newName}. Calculate electricity costs.`,
      detailedGuide: `${newName} typically use ${template.wattage * 0.8}-${template.wattage * 1.2}W. They're designed for efficient operation. In the Philippines, ${newName.toLowerCase()} are commonly used.`,
      comparison: `${newName} (${template.wattage}W) vs Standard model (${Math.round(template.wattage * 0.8)}W): Compare energy efficiency.`,
      faq: [
        `How much electricity does a ${newName.toLowerCase()} use?`,
        `Is a ${newName.toLowerCase()} energy-efficient?`,
        `What is the best ${newName.toLowerCase()} brand in the Philippines?`,
        `How can I reduce my ${newName.toLowerCase()}'s electricity consumption?`
      ]
    };
    
    newAppliances.push(appliance);
    existingSlugs.add(newSlug);
    existingNames.add(newName.toLowerCase());
    generatedCount++;
  }
}

